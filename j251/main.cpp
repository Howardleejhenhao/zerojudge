#pragma GCC optimize("O3,unroll-loops")
#pragma GCC target("avx2,bmi,bmi2,lzcnt,popcnt")

#include <bits/stdc++.h>
#define ll long long

using namespace std;

void solve()
{
    int a, b, c, aa, bb, cc;
    cin >> a >> aa >> b >> bb >> c >> cc;
    cout << (max({a, b, c}) - min({a, b, c})) * (max({aa, bb, cc}) - min({aa, bb, cc})) << '\n';
    return;
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    // cin >> t;
    while(t--)
    {
        solve();
    }
    return 0;
}
